  function initRouteMap(stations, routeColor) {
    if (!stations || stations.length === 0) return;
    var hasCoords = stations.some(function(s) { return s.lat && s.lng; });
    if (!hasCoords) return;

    var mapEl = document.getElementById('satellite-map');
    if (!mapEl) return;

    if (activeMap) {
      activeMap.remove();
      activeMap = null;
    }

    var map = L.map('satellite-map', {
      scrollWheelZoom: true,
      zoomControl: true
    });

    L.tileLayer('https://server.arcgisonline.com/ArcGIS/rest/services/World_Imagery/MapServer/tile/{z}/{y}/{x}', {
      attribution: '&copy; Esri &mdash; Source: Esri, i-cubed, USDA, USGS, AEX, GeoEye, Getmapping, Aerogrid, IGN, IGP, UPR-EGP, and the GIS User Community',
      maxZoom: 18
    }).addTo(map);

    var latlngs = [];
    stations.forEach(function(st, i) {
      if (st.lat && st.lng) {
        latlngs.push([st.lat, st.lng]);

        var markerHtml = '<div class="map-marker" style="background:' + routeColor + '">' + (i + 1) + '</div>';
        var icon = L.divIcon({
          html: markerHtml,
          className: 'map-marker-wrapper',
          iconSize: [28, 28],
          iconAnchor: [14, 14]
        });

        var popupContent = '<div class="map-popup">' +
          '<div class="mp-name">' + st.name + '</div>' +
          '<div class="mp-meta">⛰️ ' + st.altitude + ' · ' + st.distance + '</div>' +
          '<div class="mp-road">🛤️ ' + st.road + '</div>' +
          '<div class="mp-desc">' + st.desc + '</div>' +
          '</div>';

        L.marker([st.lat, st.lng], { icon: icon })
          .bindPopup(popupContent, { maxWidth: 280, className: 'map-popup-container' })
          .addTo(map);
      }
    });

    if (latlngs.length >= 2) {
      var polyline = L.polyline(latlngs, {
        color: routeColor,
        weight: 4,
        opacity: 0.8,
        smoothFactor: 1,
        dashArray: null
      }).addTo(map);

      map.fitBounds(polyline.getBounds().pad(0.08));
    } else if (latlngs.length === 1) {
      map.setView(latlngs[0], 13);
    }

    activeMap = map;
  }

